def get(data, i, imm):
    if imm == 1:
        return data[i]
    else:
        return data[data[i]]

def tick(data, i=0):
    op = data[i]
    op, imm1, imm2, imm3 = op % 100, op // 100 % 10, op // 1000 % 10, op // 10000 % 10
    if op == 99:
        return -1

    if op == 1:
        data[data[i + 3]] = get(data, i+1, imm1) + get(data, i+2, imm2)
        i += 4
    elif op == 2:
        data[data[i + 3]] = get(data, i+1, imm1) * get(data, i+2, imm2)
        i += 4
    elif op == 3:
        data[data[i + 1]] = int(input("IntMachine Input: "))
        i += 2
    elif op == 4:
        print(get(data, i+1, imm1))
        i += 2
    elif op == 5:
        if get(data, i+1, imm1) != 0:
            i = get(data, i+2, imm2)
        else:
            i += 3
    elif op == 6:
        if get(data, i+1, imm1) == 0:
            i = get(data, i+2, imm2)
        else:
            i += 3
    elif op == 7:
        if get(data, i+1, imm1) < get(data, i+2, imm2):
            data[data[i+3]] = 1
        else:
            data[data[i+3]] = 0
        i += 4
    elif op == 8:
        if get(data, i+1, imm1) == get(data, i+2, imm2):
            data[data[i+3]] = 1
        else:
            data[data[i+3]] = 0
        i += 4

    return i


